        if keys[pygame.K_d]:
            self.dx += 3
        if keys[pygame.K_SPACE] and self.jump:
            self.jump = False
            deg = self.rot * math.pi/180
            x = math.cos(deg) * 40
            y = math.sin(deg) * 40
